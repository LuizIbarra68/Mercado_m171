    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
      int linha = tableCategoria.getSelectedRow();
        if(linha == -1){
            JOptionPane.showMessageDialog(this, "Você deve selecionar uma categoria!");
            
        }else{
            ObjCategoria cat = new ObjCategoria();
            int codigo = (int) tableCategoria.getModel().getValueAt(linha, 0);
            cat.setCodigo(codigo);
            //excluir do banco:
            CategoriaDAO.excluir(cat);
            //recarregar a tabela:
            carregarTabela();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

