    private void carregarTabela(){
        DefaultTableModel modelo = new DefaultTableModel();
        String[] colunas = { "Código" , "Nome da Categoria" };
        //identificadores das colunas:
        modelo.setColumnIdentifiers(colunas);
        List<ObjCategoria> lista = CategoriaDAO.getCategorias();
        
        for(ObjCategoria cat : lista){
            Object[] obj = { cat.getCodigo(), cat.getNome() };
            modelo.addRow(obj);
            
        }
        tableCategoria.setModel(modelo);
    }
